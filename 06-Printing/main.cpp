#include <iostream>

using namespace std;

int main()
{
    // One Line
    cout << "Hello world!" << endl;

    // One line in two line code
    std::cout << "Gunadarma";
    std::cout << "University" << endl;

    // New Line
    cout << "Selamat Datang\nDi Indonesia" << endl;

    // Kompleks
    cout << "Halo sodara-sodara sekalian\n";
    cout << "ini adalah baris baru.";
    cout << "ini bukan baris baru.";
    cout << " ini akhir kalimat." << endl;
    return 0;
}
